using System;

namespace ComparativeOperators
{
    class Program
    {
        static void Main(string[] args)
        {

        int a = 10;
        
        bool c1 = a < 10;
        Console.WriteLine(c1);
        
        bool c2 = a == 10;
        Console.WriteLine(c2);
        
        bool c3 = a > 10;
        Console.WriteLine(c3);

        bool c4 = a >= 10;
        Console.WriteLine(c4);
        
        bool c5 = a <= 10;
        Console.WriteLine(c5);
        
        bool c6 = a <= 10;
        Console.WriteLine(c6);

        }
    }
}